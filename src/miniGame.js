function generateRandom(Arr){
    //实际上是对数组下标的随机生成。 
    let colorIndex=Math.floor(Math.random()*Arr.length);
    return Arr[colorIndex];
}

function randomColorMerge(Arr1,Arr2){
    let newArr,tmpArr;
    tmpArr=Arr1.concat(Arr1);
    newArr=tmpArr.concat(Arr2);
    return newArr;
}

